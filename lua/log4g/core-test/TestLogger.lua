local ctx = Log4g.API.LoggerContextFactory.GetContext("TestLoggerLogContext", true)
local lc = Log4g.Core.Config.LoggerConfig.create("LogTester", ctx:GetConfiguration(), Log4g.Core.Level.getLevel("TRACE"))
lc:AddAppender(Log4g.Core.Appender.ConsoleAppender.createConsoleAppender("TestAppender", Log4g.Core.Layout.PatternLayout.createDefaultLayout("TestLayout")))
local logger = Log4g.Core.Logger.create("LogTester", ctx, lc)
logger:Trace("Test TRACE message")
print(logger:IsAdditive())
ctx:Terminate()